package sesion4;

public class Direccion {

	String via;
	int portal;
	int piso;
	char puerta;
	String ciudad;
	String pais;
	int codigo_postal;
	
	public Direccion(String via, int portal, int piso, char puerta, String ciudad, String pais, int codigo_postal) {
		this.via = via;
		this.portal = portal;
		this.piso = piso;
		this.puerta = puerta;
		this.ciudad = ciudad;
		this.pais = pais;
		this.codigo_postal = codigo_postal;
	}
	
	
	
}
